package com.checkmarx.cxconsole.cxosa.utils.Exception;

public class OSAUtilException extends Exception {

    public OSAUtilException() {
        super();
    }

    public OSAUtilException(String message) {
        super(message);
    }

    public OSAUtilException(String message, Throwable cause) {
        super(message, cause);
    }

    public OSAUtilException(Throwable cause) {
        super(cause);
    }


}

